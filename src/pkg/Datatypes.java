package pkg;

public class Datatypes {
	public static void main(String[] args)
	{
		byte by = 20;
		int in=120;
		short sh=12457;
		long ln= 963317528l;
		float ft = 12.3f;
		double db = 4.3265;
		boolean bool=true;
		char ch= 'A';
		String st="Abhi";
		String name="Geethu AbilashI";
		System.out.println("Byte value is" + by);
		System.out.println("int value is" + in);
		System.out.println("short value is" + sh);
		System.out.println("float value is" + ft);
		System.out.println("double value is" + db);
		System.out.println("boolean value is" + bool);
		System.out.println("char value is" + ch);
		System.out.println("string value is" + st);
		System.out.println("long value is" + ln);
		System.out.println("My name is" + " "+name);
	}

}
